
# Primero tenemos que poner un identificador o etiqueta
# Despues el signo de igual que asigna un valor
# Y por ultimo el valor que le vamos a asignar
# Ejemplo:
nombre = 'Diego' # Texto
print(nombre)

edad = 23 # Numero
print(edad)

estatura = 1.75 # Numero con decimales
print(estatura)

# Estamos almacenando informacion en la memoria RAM
# Para poder utilizarla despues en el programa. Por
# otro lado se puede sobre escribir la informacion.
edad = 24
print("La edad ahora es", edad)

# Tambien podemos interactuar con el usuario
# Para ello utilizamos la funcion input()
# Ejemplo:
nombre = input('Por favor ingresa tu nombre: ')
print('Hola', nombre, 'bienvenido a Python!')
