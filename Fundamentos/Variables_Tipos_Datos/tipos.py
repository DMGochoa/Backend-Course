# Las cadenas de texto se pueden definir con comillas simples 
# o dobles, pero no se pueden mezclar.

nombre = 'Diego'
nombre = "Diego"
print(f"Hola '{nombre}'")
print('El tipo de dato de la variable nombre es:', type(nombre))
# int
edad = 23
print('El tipo de dato de la variable edad es:', type(edad))
# float
estatura = 1.75
print('El tipo de dato de la variable estatura es:', type(estatura))
# bool
tiene_mascota = True
print('El tipo de dato de la variable tiene_mascota es:', type(tiene_mascota))